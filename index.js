function add(a, b){
  var sum = a + b;
  return sum;
}
function subtract(a, b){
  var sum = a - b;
  return sum; 
}
function multiply(a, b){
  var sum = a * b;
  return sum;
}
function divide(a, b){
  var sum = a / b;
  return sum;
}
function inc(a){
  var sum = a;
  sum++;
  return sum;
}

function dec(a){
  var sum = a;
  sum--;
  return sum;
}